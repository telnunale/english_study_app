      allVerbs.where((v) => !v.isIrregular).toList();

  List<Verb> searchVerbs(String query) {
    final q = query.toLowerCase();
    return allVerbs
        .where(
          (v) =>
              v.infinitive.toLowerCase().contains(q) ||
              v.spanishMeaning.toLowerCase().contains(q),
        )
        .toList();
  }
